#include <stdio.h>

int main() {
    int num;
    printf("Enter the number \n");
    scanf("%d", &num);
    if(num%2 == 0){

        printf("\n The given number is even \n");
        }
    else{
        printf("\n The given number is odd \n");
    }
    return 0;
    }

